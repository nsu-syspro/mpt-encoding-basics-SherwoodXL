#!/usr/bin/env bash

# Вывести время в формате:
#   15 час/а/ов 23 минут/ы
hours=$(date -d "$@" +%H)
minutes=$(date -d "$@" +%M)
case $hours in
    1|21) h=$"час" ;;
    2|3|4|22|23) h=$"часа" ;;
    *) h=$"часов" ;;
esac
case $minutes in
    1|21|31|41|51) m=$"минута" ;;
    2|3|4|22|23|24|32|33|34|42|43|44|52|53|54) m=$"минуты" ;;
    *) m=$"минут" ;;
esac
echo "$hours $h $minutes $m"